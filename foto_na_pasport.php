					<p class="uk-text-small">На основании действующего законодательства разрешено передавать снимок на бумажном носителе или в электронном формате. В исполнительные органы разрешено передавать снимок на съемном носителе, либо через интернет. Для электронного формата существуют следующие стандарты:</p>
