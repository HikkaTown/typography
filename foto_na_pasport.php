					<p class="uk-text-small">Допустимо только использование белого фона. Новое требование пришло на смену допустимого светлого тона. На готовом фотоснимке не должно присутствовать никаких предметов, отблесков или теней: только белый фон и изображение человека.</p>
					<h2 >Что выбрать: глянец или матовую бумагу</h2>
					<p class="uk-text-small">Существующим регламентом не предусмотрено конкретное правило касательно выбора бумаги: глянцевой или матовой. Клиент может самостоятельно делать выбор. Глянец позволяет надолго сохранить первоначальные сочные оттенки. Чтобы получить российский паспорт, уголки не потребуются.</p>
					<h2 >Правила для изображения</h2>
					<p class="uk-text-small">Прежде чем искать фото на паспорт рядом со мной, важно понимать, каким обязательным требованиям должен соответствовать снимок:</p>
					<ul>
						<li>•	лицо в анфас;</li>
						<li>•	голову нельзя поворачивать или наклонять;</li>
						<li>•	лицо не должно быть напряженным. Нужно сохранять естественную мимику;</li>
						<li>•	сохранять взгляд в объектив;</li>
						<li>•	отсутствие сжатых губ или улыбки;</li>
						<li>•	80% снимка должно занимать лицо;</li>
						<li>•	32–36  мм высота головы;</li>
						<li>•	18–25  мм ширина головы;</li>
						<li>•	полный захват лица;</li>
						<li>•	расстояние между зрачками составляет 7 мм;</li>
						<li>•	между концом снимка и началом головы расстояние 5 мм.</li>
					</ul>
